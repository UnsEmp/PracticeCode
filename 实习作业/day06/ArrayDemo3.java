package day06;

/**
 *  二维数组静态初始化：
 *      完整格式：数据类型[][] 变量名 = new 数据类型[][]{{元素1，元素2....},{元素1，元素2....},{元素1，元素2....}......};
 *      简化格式：数据类型[][] 变量名 = {{元素1，元素2....},{元素1，元素2....},{元素1，元素2....}......};
 */
public class ArrayDemo3 {
    public static void main(String[] args) {
        int arr[][] = new int[][]{{1,2,3},{4,5},{6}};

        int arr1[][] = {{1,2,3},{4,5},{6}};
    }
}